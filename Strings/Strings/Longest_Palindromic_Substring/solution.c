#include<stdio.h>
int main()
{
	char str[100];int i;int j;int len;int max=0;int start;
	scanf("%s",str);
	for(i=0;str[i]!='\0';i++)
	{
		for(j=0;i-j>=0&&str[i+j]!='\0';j++)
		{
			if(str[i+j]!=str[i-j])
			{
				break;
			}
			len=2*j+1;
			if(len>max)
			{
				max=len;
				start=i-j;
			}
		}
		for(j=0;i-j>=0&&str[i+j+1]!='\0';j++)
		{
			if(str[i-j]!=str[i+j+1])
			{
				break;
			}
			len=2*j+2;
			if(len>max)
			{
				max=len;
				start=i-j;
			}
		}
	}
	for(i=start;i<start+max;i++)
	{
		printf("%c",str[i]);
	}
}
